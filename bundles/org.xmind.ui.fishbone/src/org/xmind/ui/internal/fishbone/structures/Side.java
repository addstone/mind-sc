package org.xmind.ui.internal.fishbone.structures;

class Side {

    double lastJoint;

    double rotatedBottom;

    double right;

    boolean useRight = false;

    void start(double x) {
        this.right = x;
        this.rotatedBottom = x;
        this.lastJoint = 0;
        this.useRight = false;
    }

}