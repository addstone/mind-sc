    public static String getHexRGBString(String colorName) {
        if (null == colorName || "".equals(colorName) //$NON-NLS-1$
                || SVGDefinitionConstants.NONE.equals(colorName))
            return null;

        for (ColorName2HexRGBValue colorName2RGB : values())
            if (colorName2RGB.getName().equals(colorName))
                return colorName2RGB.getRgb();

        return null;
    }

